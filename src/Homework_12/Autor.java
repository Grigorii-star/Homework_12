package Homework_12;

public class Autor {
    private String name;
    private String surName;

    public Autor(String name, String surName) {
        this.name = name;
        this.surName = surName;
    }

    public String getName() {
        return this.name;
    }

    public String getSurName() {
        return this.surName;
    }
}
